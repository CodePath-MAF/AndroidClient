
package org.missionassetfund.apps.android.models;

public class LendingCircleFriend {

    String name;
    int dpDrawableId;

    public LendingCircleFriend(String name, int dpDrawableId) {
        this.name = name;
        this.dpDrawableId = dpDrawableId;
    }

    public String getName() {
        return name;
    }

    public int getDpDrawableId() {
        return dpDrawableId;
    }

}
